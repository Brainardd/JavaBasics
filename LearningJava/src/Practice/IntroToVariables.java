package Practice;

public class IntroToVariables {
	public static void main(String[] args) {
		
		int x = 1;
		int y = x + 1;
		
		System.out.println("This variable is x: " + x);
		System.out.println("This variable is y: " + y);
		
		
		// Another example of "int"
		
		int i;
		int j;
		
		i = 2;
		j = i * 2;
		
		System.out.println("This variable is i: " + i);
		System.out.println("This variable is j: " + j);
		
		
		// Another example of "string"
		
		String a = "Jebi";
		String b = "Dana";
		
		System.out.println(a + " loves " + b + " so much!");
		
		
		// Another example of "double"
		
		double Pie = 3.14;
		
		System.out.println(Pie);
		
	}
	
}
